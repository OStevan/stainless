object IntComparison {

  def less(x: Int, y: Int): Boolean = {
    x < y
  }

  def lessEq(x: Int, y: Int): Boolean = {
    x <= y
  }

  def greater(x: Int, y: Int): Boolean = {
    x > y
  }

  def greaterEq(x: Int, y: Int): Boolean = {
    x >= y
  }

  def equal(x: Int, y: Int): Boolean = {
    x == y
  }
}